<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				yishenggonghao: '',
				yishengxingming: '',
				keshi: '',
				zhicheng: '',
				xingbie: '',
				yiling: '',
				guahaofeiyong: '',
				yishengdianhua: '',
				touxiang: '',
				paibanshiduan: '',
				yishengjianjie: '',
				},
				keshiOptions: [],
				keshiIndex: 0,
				zhichengOptions: [],
				zhichengIndex: 0,
				xingbieOptions: [],
				xingbieIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   yishenggonghao : false,
                   yishengxingming : false,
                   keshi : false,
                   zhicheng : false,
                   xingbie : false,
                   yiling : false,
                   guahaofeiyong : false,
                   yishengdianhua : false,
                   touxiang : false,
                   paibanshiduan : false,
                   yishengjianjie : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下拉框
			res = await this.$api.option(`keshi`,`keshi`,{});
			this.keshiOptions = res.data;
            this.keshiOptions.unshift("请选择科室");
			// 自定义下拉框值
			this.zhichengOptions = "专家,主任医师,副主任医师,主治医师".split(',')
			// 自定义下拉框值
			this.xingbieOptions = "男,女".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zuozhenyisheng`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yishenggonghao'){
					this.ruleForm.yishenggonghao = obj[o];
					this.ro.yishenggonghao = true;
					continue;
					}
					if(o=='yishengxingming'){
					this.ruleForm.yishengxingming = obj[o];
					this.ro.yishengxingming = true;
					continue;
					}
					if(o=='keshi'){
					this.ruleForm.keshi = obj[o];
					this.ro.keshi = true;
					continue;
					}
					if(o=='zhicheng'){
					this.ruleForm.zhicheng = obj[o];
					this.ro.zhicheng = true;
					continue;
					}
					if(o=='xingbie'){
					this.ruleForm.xingbie = obj[o];
					this.ro.xingbie = true;
					continue;
					}
					if(o=='yiling'){
					this.ruleForm.yiling = obj[o];
					this.ro.yiling = true;
					continue;
					}
					if(o=='guahaofeiyong'){
					this.ruleForm.guahaofeiyong = obj[o];
					this.ro.guahaofeiyong = true;
					continue;
					}
					if(o=='yishengdianhua'){
					this.ruleForm.yishengdianhua = obj[o];
					this.ro.yishengdianhua = true;
					continue;
					}
					if(o=='touxiang'){
					this.ruleForm.touxiang = obj[o].split(",")[0];
					this.ro.touxiang = true;
					continue;
					}
					if(o=='paibanshiduan'){
					this.ruleForm.paibanshiduan = obj[o];
					this.ro.paibanshiduan = true;
					continue;
					}
					if(o=='yishengjianjie'){
					this.ruleForm.yishengjianjie = obj[o];
					this.ro.yishengjianjie = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
			
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
            yishengjianjieChange(e) {
                this.ruleForm.yishengjianjie = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数



			// 下拉变化
			keshiChange(e) {
				this.keshiIndex = e.target.value
				this.ruleForm.keshi = this.keshiOptions[this.keshiIndex]
			},
			// 下拉变化
			zhichengChange(e) {
				this.zhichengIndex = e.target.value
				this.ruleForm.zhicheng = this.zhichengOptions[this.zhichengIndex]
			},
			// 下拉变化
			xingbieChange(e) {
				this.xingbieIndex = e.target.value
				this.ruleForm.xingbie = this.xingbieOptions[this.xingbieIndex]
			},

			touxiangTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.touxiang = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
//跨表计算判断
				var obj;
				if((!this.ruleForm.guahaofeiyong)){
					this.$utils.msg(`挂号费用不能为空`);
					return
				}
				if(this.ruleForm.guahaofeiyong&&(!this.$validate.isIntNumer(this.ruleForm.guahaofeiyong))){
					this.$utils.msg(`挂号费用应输入整数`);
					return
				}
				if(this.ruleForm.yishengdianhua&&(!this.$validate.isMobile(this.ruleForm.yishengdianhua))){
					this.$utils.msg(`医生电话应输入手机格式`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
                    uni.setStorageSync('crossCleanType',true);
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('appUserid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`zuozhenyisheng`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
                        uni.removeStorageSync('crossCleanType');
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`zuozhenyisheng`, this.ruleForm);
						}else{
							await this.$api.add(`zuozhenyisheng`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`zuozhenyisheng`, this.ruleForm);
					}else{
						await this.$api.add(`zuozhenyisheng`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			}
		}
	}
