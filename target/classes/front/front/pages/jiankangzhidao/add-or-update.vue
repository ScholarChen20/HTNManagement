<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				zhidaobiaoti: '',
				zhidaoleixing: '',
				zhidaofangan: '',
				zhidaojianyi: '',
				zhidaotupian: '',
				huanzhezhanghao: '',
				huanzhexingming: '',
				yishenggonghao: '',
				yishengxingming: '',
				zhidaoshijian: '',
				xueyamubiaozhi: '',
				},
				zhidaoleixingOptions: [],
				zhidaoleixingIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   zhidaobiaoti : false,
                   zhidaoleixing : false,
                   zhidaofangan : false,
                   zhidaojianyi : false,
                   zhidaotupian : false,
                   huanzhezhanghao : false,
                   huanzhexingming : false,
                   yishenggonghao : false,
                   yishengxingming : false,
                   zhidaoshijian : false,
                   xueyamubiaozhi : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.zhidaoshijian = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 自定义下拉框值
			this.zhidaoleixingOptions = "饮食,运动,作息,情绪".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jiankangzhidao`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='zhidaobiaoti'){
					this.ruleForm.zhidaobiaoti = obj[o];
					this.ro.zhidaobiaoti = true;
					continue;
					}
					if(o=='zhidaoleixing'){
					this.ruleForm.zhidaoleixing = obj[o];
					this.ro.zhidaoleixing = true;
					continue;
					}
					if(o=='zhidaofangan'){
					this.ruleForm.zhidaofangan = obj[o];
					this.ro.zhidaofangan = true;
					continue;
					}
					if(o=='zhidaojianyi'){
					this.ruleForm.zhidaojianyi = obj[o];
					this.ro.zhidaojianyi = true;
					continue;
					}
					if(o=='zhidaotupian'){
					this.ruleForm.zhidaotupian = obj[o].split(",")[0];
					this.ro.zhidaotupian = true;
					continue;
					}
					if(o=='huanzhezhanghao'){
					this.ruleForm.huanzhezhanghao = obj[o];
					this.ro.huanzhezhanghao = true;
					continue;
					}
					if(o=='huanzhexingming'){
					this.ruleForm.huanzhexingming = obj[o];
					this.ro.huanzhexingming = true;
					continue;
					}
					if(o=='yishenggonghao'){
					this.ruleForm.yishenggonghao = obj[o];
					this.ro.yishenggonghao = true;
					continue;
					}
					if(o=='yishengxingming'){
					this.ruleForm.yishengxingming = obj[o];
					this.ro.yishengxingming = true;
					continue;
					}
					if(o=='zhidaoshijian'){
					this.ruleForm.zhidaoshijian = obj[o];
					this.ro.zhidaoshijian = true;
					continue;
					}
					if(o=='xueyamubiaozhi'){
					this.ruleForm.xueyamubiaozhi = obj[o];
					this.ro.xueyamubiaozhi = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
			
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			zhidaoshijianChange(e) {
				this.ruleForm.zhidaoshijian = e.target.value;
				this.$forceUpdate();
			},


			// 下拉变化
			zhidaoleixingChange(e) {
				this.zhidaoleixingIndex = e.target.value
				this.ruleForm.zhidaoleixing = this.zhidaoleixingOptions[this.zhidaoleixingIndex]
			},

			zhidaotupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.zhidaotupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
//跨表计算判断
				var obj;
				if((!this.ruleForm.zhidaobiaoti)){
					this.$utils.msg(`指导标题不能为空`);
					return
				}
				if((!this.ruleForm.zhidaotupian)){
					this.$utils.msg(`指导图片不能为空`);
					return
				}
				if(this.ruleForm.xueyamubiaozhi&&(!this.$validate.isNumber(this.ruleForm.xueyamubiaozhi))){
					this.$utils.msg(`血压目标值(mmHg)应输入数字`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
                    uni.setStorageSync('crossCleanType',true);
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('appUserid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`jiankangzhidao`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
                        uni.removeStorageSync('crossCleanType');
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`jiankangzhidao`, this.ruleForm);
						}else{
							await this.$api.add(`jiankangzhidao`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`jiankangzhidao`, this.ruleForm);
					}else{
						await this.$api.add(`jiankangzhidao`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			}
		}
	}
